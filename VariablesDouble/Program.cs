
// Zmienna typu double zawiera liczby z miejscami po przecinku np.: 1,23.


// Inicjalizacja zmiennej typu double
double sredniaWieku;

// Aby zapisywać liczby dziseiętne należy używać kropki do rozdzielenia liczby o miejsc po przecinku
sredniaWieku = (42.0 + 23 + 62) / 3;

Console.WriteLine(sredniaWieku);

